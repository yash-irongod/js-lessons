// Operators
let a = 10;
let b = 5;
console.log("a - b =", a - b);


// --------------------
// if-else
// --------------------
let x = 30;
let y = 20;

if (x < y) {
    console.log("x is smaller");
} else {
    console.log("x is greater");
}


// --------------------
// for loop
// --------------------
for (let i = 1; i <= 5; i++) {
    console.log("for loop:", i);
}


// --------------------
// while loop
// --------------------
let count = 1;
while (count <= 3) {
    console.log("while loop:", count);
    count++;
}
